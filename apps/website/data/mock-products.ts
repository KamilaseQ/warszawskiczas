/**
 * Kanoniczny URL produktu — zawsze w formie "marka-model" (kebab-case),
 * niezależnie od wewnętrznego `slug`.
 */
export function productUrlSlug(p: Pick<Product, 'brand' | 'name'>): string {
  const raw = `${p.brand} ${p.name}`
  return raw
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/ł/g, 'l')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}

export function findProductByUrlSlug(slug: string): Product | undefined {
  return mockProducts.find((p) => productUrlSlug(p) === slug)
}

