/**
 * Kanoniczny URL produktu — zawsze w formie "marka-model" (kebab-case),
 * niezależnie od wewnętrznego `slug`.
 */
function productUrlSlug(p) {
    const raw = `${p.brand} ${p.name}`;
    return raw
        .toLowerCase()
        .normalize('NFD')
        .replace(/[̀-ͯ]/g, '')
        .replace(/ł/g, 'l')
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-+|-+$/g, '');
}
function findProductByUrlSlug(slug) {
    return exports.mockProducts.find((p) => productUrlSlug(p) === slug);
}
